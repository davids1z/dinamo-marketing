"""PDF report generator using reportlab.

Generates branded weekly and monthly marketing performance PDFs.
ShiftOneZero Marketing Platform
"""

import io
import logging
from datetime import datetime

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, HRFlowable,
)

logger = logging.getLogger(__name__)

# Brand colours
NAVY = colors.HexColor("#0A1A28")
ACCENT = colors.HexColor("#B8FF00")
BLUE = colors.HexColor("#0057A8")
LIGHT_GRAY = colors.HexColor("#F3F4F6")
MEDIUM_GRAY = colors.HexColor("#9CA3AF")

PAGE_W, PAGE_H = A4


def _styles():
    """Build custom paragraph styles."""
    base = getSampleStyleSheet()
    base.add(ParagraphStyle(
        "Title_Custom", parent=base["Title"],
        fontName="Helvetica-Bold", fontSize=20, textColor=NAVY,
        spaceAfter=6,
    ))
    base.add(ParagraphStyle(
        "Subtitle", parent=base["Normal"],
        fontName="Helvetica", fontSize=11, textColor=MEDIUM_GRAY,
        spaceAfter=12,
    ))
    base.add(ParagraphStyle(
        "SectionHead", parent=base["Heading2"],
        fontName="Helvetica-Bold", fontSize=13, textColor=BLUE,
        spaceBefore=16, spaceAfter=8,
    ))
    base.add(ParagraphStyle(
        "BodyText_Custom", parent=base["Normal"],
        fontName="Helvetica", fontSize=10, leading=14,
        textColor=NAVY, spaceAfter=6,
    ))
    base.add(ParagraphStyle(
        "SmallGray", parent=base["Normal"],
        fontName="Helvetica", fontSize=8, textColor=MEDIUM_GRAY,
    ))
    return base


def _metric_table(data: list[list], col_widths=None):
    """Create a styled metric table."""
    t = Table(data, colWidths=col_widths, hAlign="LEFT")
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), NAVY),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, 0), 9),
        ("FONTNAME", (0, 1), (-1, -1), "Helvetica"),
        ("FONTSIZE", (0, 1), (-1, -1), 9),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, LIGHT_GRAY]),
        ("GRID", (0, 0), (-1, -1), 0.5, MEDIUM_GRAY),
        ("TOPPADDING", (0, 0), (-1, -1), 4),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
        ("LEFTPADDING", (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
        ("ALIGN", (1, 0), (-1, -1), "RIGHT"),
    ]))
    return t


def _kpi_row(styles, items: list[tuple[str, str]]):
    """Create a horizontal KPI summary row as a table."""
    header = [Paragraph(f"<b>{label}</b>", styles["SmallGray"]) for label, _ in items]
    values = [Paragraph(f"<b>{value}</b>", styles["BodyText_Custom"]) for _, value in items]
    t = Table([header, values], colWidths=[120] * len(items), hAlign="LEFT")
    t.setStyle(TableStyle([
        ("ALIGN", (0, 0), (-1, -1), "CENTER"),
        ("TOPPADDING", (0, 0), (-1, -1), 4),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
    ]))
    return t


def _footer(canvas, doc):
    canvas.saveState()
    canvas.setFont("Helvetica", 7)
    canvas.setFillColor(MEDIUM_GRAY)
    canvas.drawString(
        20 * mm, 10 * mm,
        f"Generated by ShiftOneZero Marketing Platform — {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}",
    )
    canvas.drawRightString(PAGE_W - 20 * mm, 10 * mm, f"Page {doc.page}")
    canvas.restoreState()


def _fmt(n, prefix="", suffix="") -> str:
    if isinstance(n, float):
        return f"{prefix}{n:,.2f}{suffix}"
    if isinstance(n, int):
        return f"{prefix}{n:,}{suffix}"
    return f"{prefix}{n}{suffix}"


